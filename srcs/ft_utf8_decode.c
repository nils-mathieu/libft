uint32_t	ft_utf8_decode(uint32_t state, uint32_t *codep, uint32_t byte)
{
	const uint32_t	type = g_utf8d[byte];

	if (state)
		*codep = (byte & 0x3fu) | (*codep << 6);
	else
		*codep = (0xff >> type) & (byte);
	state = g_utf8d[256 + state * 16 + type];
	return (state);
}
